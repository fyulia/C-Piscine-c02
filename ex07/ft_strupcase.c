char	*ft_strupcase(char *str)
{
	int		i;
	char	temp;

	i = 0;
	while (str[i] != '\0')
	{
		tmp = str[i];
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = tmp - 32;
		}
		i++;
	}
	return (str);
}
