char	*ft_strlowcase(char *str)
{
	int		i;
	char	tmp;

	i = 0;
	while (str[i] != '\0')
	{
		tmp = str[i];
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = tmp + 32;
		}
		i++;
	}
	return (str);
}
